// Просим ввести число
Console.WriteLine("Введите число");
// Считываем с консоли и сразу переводим в число
int N = int.Parse(Console.ReadLine()??"0");
// Задаем условие чтобы число попадало в диапазон от 1 до 7
if(N>0 && N<8)
{// Если число больше 5 выводим в консоль 
    if(N>5) Console.WriteLine("Это выходной");
    else Console.WriteLine("Это не выходной");
}
// Если число не попадает в диапазон
else
   Console.WriteLine("Не является днем недели, введите другое число");
